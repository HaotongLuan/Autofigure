class FigureEvaluator:
    """Evaluates scientific figures using VLM-as-Judge with the Claude API.

    Usage:
        evaluator = FigureEvaluator(api_key="...")
        result = evaluator.evaluate_single(text, svg, ground_truth)
        comp = evaluator.evaluate_comparison(text, svg_a, svg_b)
        batch = evaluator.batch_evaluate(test_cases, results)
    """

    def __init__(
        self,
        api_key: Optional[str] = None,
        model: str = "claude-sonnet-4-6",
    ):
        """Initialize the evaluator with an Anthropic API key.

        Args:
            api_key: Anthropic API key. If None, uses the ANTHROPIC_API_KEY
                environment variable or the SDK default resolution.
            model: Claude model identifier. Default: "claude-sonnet-4-6".
        """
        self._client = _ClaudeClient(api_key=api_key, model=model)
        self._model = model

    # ------------------------------------------------------------------
    # Single evaluation
    # ------------------------------------------------------------------

    def evaluate_single(
        self,
        text: str,
        svg: str,
        ground_truth: Optional[dict] = None,
    ) -> dict[str, Any]:
        """Evaluate one generated figure against the source text.

        Args:
            text: The source paper excerpt describing the system/method.
            svg: The generated SVG figure content as a string.
            ground_truth: Optional dict with "key_entities", "key_relationships",
                and "description" fields for reference.

        Returns:
            A dict with scores (0-10), issues list, and a Chinese summary.
            See module docstring for full shape.
        """
        truncated_svg = _truncate_svg(svg)

        # Build the user prompt
        parts: list[str] = []
        parts.append("=== SOURCE TEXT ===")
        parts.append(text)
        parts.append("")
        parts.append("=== GENERATED SVG FIGURE ===")
        parts.append(truncated_svg)

        if ground_truth:
            parts.append("")
            parts.append("=== GROUND TRUTH (for reference) ===")
            parts.append(json.dumps(ground_truth, ensure_ascii=False, indent=2))

        parts.append("")
        parts.append(
            "Please evaluate the generated figure against the source text. "
            "Provide your response as a single JSON object with scores, issues, "
            "and a Chinese summary."
        )

        user_content = "\n".join(parts)

        raw_response = self._retry_json_call(
            system_prompt=SYSTEM_PROMPT_SINGLE,
            user_content=user_content,
        )

        result = self._parse_json_response(raw_response, "evaluate_single")
        result = _validate_scores(result, tuple(SCORE_DIMENSIONS))

        return result

    # ------------------------------------------------------------------
    # Comparison evaluation
    # ------------------------------------------------------------------

    def evaluate_comparison(
        self,
        text: str,
        svg_a: str,
        svg_b: str,
        ground_truth: Optional[dict] = None,
    ) -> dict[str, Any]:
        """Blind comparison of two generated figures.

        The figures are randomly assigned as "Figure A" and "Figure B"
        internally to ensure blind evaluation. The returned result
        references the randomized labels, and the mapping is included
        so the caller can determine which original figure won.

        Args:
            text: The source paper excerpt.
            svg_a: First generated SVG.
            svg_b: Second generated SVG.
            ground_truth: Optional ground truth annotations.

        Returns:
            A dict with:
            - winner: "A" or "B" or "tie" (referring to randomized labels)
            - scores: dict with "A" and "B" keys, each containing score dicts
            - comparison_notes: Chinese comparison summary
            - a_better_than_b_on: dimensions A won
            - b_better_than_a_on: dimensions B won
            - randomized_mapping: {"A": "svg_a" or "svg_b", "B": the other}
        """
        # Randomly assign which SVG gets which label
        swap = random.choice([False, True])
        if swap:
            figure_a_content = svg_b
            figure_b_content = svg_a
            mapping = {"A": "svg_b", "B": "svg_a"}
        else:
            figure_a_content = svg_a
            figure_b_content = svg_b
            mapping = {"A": "svg_a", "B": "svg_b"}

        truncated_a = _truncate_svg(figure_a_content)
        truncated_b = _truncate_svg(figure_b_content)

        parts: list[str] = []
        parts.append("=== SOURCE TEXT ===")
        parts.append(text)
        parts.append("")
        parts.append("=== FIGURE A ===")
        parts.append(truncated_a)
        parts.append("")
        parts.append("=== FIGURE B ===")
        parts.append(truncated_b)

        if ground_truth:
            parts.append("")
            parts.append("=== GROUND TRUTH (for reference) ===")
            parts.append(json.dumps(ground_truth, ensure_ascii=False, indent=2))

        parts.append("")
        parts.append(
            "Please evaluate both figures independently and compare them. "
            "Provide your response as a single JSON object."
        )

        user_content = "\n".join(parts)

        raw_response = self._retry_json_call(
            system_prompt=SYSTEM_PROMPT_COMPARISON,
            user_content=user_content,
        )

        result = self._parse_json_response(raw_response, "evaluate_comparison")

        # Validate the nested scores
        if "scores" not in result or not isinstance(result["scores"], dict):
            result["scores"] = {"A": {}, "B": {}}

        for label in ("A", "B"):
            if label not in result["scores"]:
                result["scores"][label] = {}
            result["scores"][label] = _validate_scores(
                result["scores"][label],
                tuple(SCORE_DIMENSIONS),
            )

        # Ensure required top-level keys
        result.setdefault("winner", "tie")
        result.setdefault("a_better_than_b_on", [])
        result.setdefault("b_better_than_a_on", [])
        result.setdefault("comparison_notes", "")
        result["randomized_mapping"] = mapping

        return result

    # ------------------------------------------------------------------
    # Batch evaluation
    # ------------------------------------------------------------------

    def batch_evaluate(
        self,
        test_cases: list[dict],
        results: dict[str, dict],
    ) -> dict[str, Any]:
        """Evaluate all test cases and aggregate metrics.

        Args:
            test_cases: List of test case dicts (each with "id", "text",
                "figure_type", "ground_truth").
            results: Dict mapping test_case_id to {"svg": str, "method": str}.
                Each test_case_id can map to a list of result dicts if multiple
                methods generated figures for the same test case.

        Returns:
            Aggregated metrics dict:
            {
                "per_case": {tc_id: {evaluation dict}},
                "aggregate": {
                    "by_dimension": {dim: stats_dict},
                    "by_type": {figure_type: {dim: stats_dict}},
                    "by_method": {method: {dim: stats_dict}},
                },
                "summary": "Chinese batch evaluation summary",
            }
        """
        per_case: dict[str, Any] = {}
        all_scores: dict[str, list[float]] = defaultdict(list)
        by_type: dict[str, dict[str, list[float]]] = defaultdict(
            lambda: defaultdict(list)
        )
        by_method: dict[str, dict[str, list[float]]] = defaultdict(
            lambda: defaultdict(list)
        )

        # Build lookup for ground truth
        tc_map: dict[str, dict] = {tc["id"]: tc for tc in test_cases}

        for tc_id, result_entry in results.items():
            tc = tc_map.get(tc_id)
            if tc is None:
                per_case[tc_id] = {
                    "error": f"Unknown test case ID: {tc_id}",
                }
                continue

            svg = result_entry.get("svg", "")
            method = result_entry.get("method", "unknown")

            try:
                evaluation = self.evaluate_single(
                    text=tc["text"],
                    svg=svg,
                    ground_truth=tc.get("ground_truth"),
                )
            except Exception as exc:
                evaluation = {
                    "error": str(exc),
                    "traceback": traceback.format_exc(),
                }
                for dim in SCORE_DIMENSIONS:
                    evaluation[dim] = 0

            evaluation["method"] = method
            evaluation["figure_type"] = tc.get("figure_type", "unknown")
            per_case[tc_id] = evaluation

            # Collect scores for aggregation (skip errors)
            if "error" not in evaluation:
                for dim in SCORE_DIMENSIONS:
                    score = evaluation.get(dim, 0)
                    all_scores[dim].append(score)
                    by_type[tc.get("figure_type", "unknown")][dim].append(score)
                    by_method[method][dim].append(score)

        # Compute aggregate statistics
        aggregate: dict[str, Any] = {
            "by_dimension": {
                dim: _compute_stats(all_scores.get(dim, []))
                for dim in SCORE_DIMENSIONS
            },
            "by_type": {
                ftype: {
                    dim: _compute_stats(scores.get(dim, []))
                    for dim in SCORE_DIMENSIONS
                }
                for ftype, scores in by_type.items()
            },
            "by_method": {
                method: {
                    dim: _compute_stats(scores.get(dim, []))
                    for dim in SCORE_DIMENSIONS
                }
                for method, scores in by_method.items()
            },
        }

        # Generate a summary in Chinese
        summary_parts: list[str] = []
        summary_parts.append(f"批量评估完成，共 {len(results)} 个测试用例。")
        for dim in SCORE_DIMENSIONS:
            stats = aggregate["by_dimension"][dim]
            cn_name = SCORE_DIMENSIONS_CN.get(dim, dim)
            summary_parts.append(
                f"{cn_name}: 均值={stats['mean']}, 标准差={stats['std']}, "
                f"范围=[{stats['min']}, {stats['max']}], 样本数={stats['count']}"
            )

        if len(by_method) > 1:
            summary_parts.append(
                f"共 {len(by_method)} 种方法参与评估: {', '.join(sorted(by_method.keys()))}。"
            )

        return {
            "per_case": per_case,
            "aggregate": aggregate,
            "summary": "\n".join(summary_parts),
        }

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _retry_json_call(
        self,
        system_prompt: str,
        user_content: str,
    ) -> str:
        """Call Claude with retry specifically for JSON parse failures.

        If the response is not valid JSON, we retry with an additional
        instruction to output only valid JSON.
        """
        last_response: Optional[str] = None
        last_error: Optional[Exception] = None

        for attempt in range(1, MAX_RETRIES + 1):
            try:
                response = self._client.send_message(
                    system_prompt=system_prompt,
                    user_content=user_content,
                    temperature=0.1,
                    max_tokens=4096,
                )
            except Exception as exc:
                last_error = exc
                if attempt < MAX_RETRIES:
                    wait = RETRY_DELAY_SECONDS * attempt + random.uniform(
                        0, JITTER_SECONDS
                    )
                    time.sleep(wait)
                    continue
                raise RuntimeError(
                    f"Claude API call failed after {MAX_RETRIES} retries: {exc}"
                ) from exc

            last_response = response

            # Try to parse as JSON
            extracted = _extract_json_from_response(response)
            try:
                json.loads(extracted)
                return extracted  # Valid JSON – we are done
            except json.JSONDecodeError as exc:
                last_error = exc
                if attempt < MAX_RETRIES:
                    # Add a stern reminder to the prompt for the retry
                    user_content = (
                        user_content
                        + "\n\n[SYSTEM NOTE: Your previous response was NOT valid JSON. "
                        + f"Error: {exc}. "
                        + "You MUST output ONLY a single valid JSON object. "
                        + "No markdown fences, no surrounding text, no trailing commas, "
                        + "no comments. ONLY valid JSON.]"
                    )
                    wait = RETRY_DELAY_SECONDS * attempt + random.uniform(
                        0, JITTER_SECONDS
                    )
                    time.sleep(wait)
                    continue

        # All retries exhausted
        raise RuntimeError(
            f"Failed to get valid JSON after {MAX_RETRIES} retries. "
            f"Last JSON error: {last_error}. "
            f"Last raw response (first 500 chars): {str(last_response)[:500]}"
        )

    @staticmethod
    def _parse_json_response(raw: str, method_name: str) -> dict[str, Any]:
        """Parse the JSON response, with a fallback for partial extraction."""
        extracted = _extract_json_from_response(raw)
        try:
            result = json.loads(extracted)
            if not isinstance(result, dict):
                raise ValueError(f"Expected JSON object, got {type(result).__name__}")
            return result
        except (json.JSONDecodeError, ValueError) as exc:
            raise RuntimeError(
                f"{method_name}: Failed to parse JSON response. "
                f"Error: {exc}. "
                f"Raw (first 800 chars): {raw[:800]}"
            ) from exc
