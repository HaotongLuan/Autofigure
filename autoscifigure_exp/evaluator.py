class _ClaudeClient:
    """Thin wrapper around the Anthropic Python SDK with retry logic."""

    def __init__(
        self,
        api_key: Optional[str] = None,
        model: str = "claude-sonnet-4-6",
        max_retries: int = MAX_RETRIES,
    ):
        if anthropic is None or Anthropic is None:
            raise ImportError(
                "The 'anthropic' package is required for FigureEvaluator. "
                "Install it with: pip install anthropic"
            )

        self._model = model
        self._max_retries = max_retries

        # Resolve API key: explicit arg > env var > .netrc / default
        resolved_key = api_key
        if resolved_key is None:
            # Let the SDK handle its default resolution (env, config, etc.)
            resolved_key = None

        self._client = Anthropic(api_key=resolved_key)

    def send_message(
        self,
        system_prompt: str,
        user_content: str,
        temperature: float = 0.1,
        max_tokens: int = 4096,
    ) -> str:
        """Send a message to Claude and return the text response.

        Implements automatic retry on transient failures and JSON parse errors
        during the calling code's subsequent processing.
        """
        last_error: Optional[Exception] = None

        for attempt in range(1, self._max_retries + 1):
            try:
                message = self._client.messages.create(
                    model=self._model,
                    max_tokens=max_tokens,
                    temperature=temperature,
                    system=system_prompt,
                    messages=[
                        {"role": "user", "content": user_content},
                    ],
                )

                # Extract text from the first text block
                text_blocks = [
                    block.text
                    for block in message.content
                    if getattr(block, "type", None) == "text"
                ]
                if not text_blocks:
                    raise RuntimeError(
                        f"No text content in response: {message.content}"
                    )
                return "".join(text_blocks)

            except (anthropic.APIError, anthropic.APIConnectionError,
                    anthropic.RateLimitError, anthropic.APITimeoutError) as exc:
                last_error = exc
                if attempt < self._max_retries:
                    wait = RETRY_DELAY_SECONDS * attempt + random.uniform(
                        0, JITTER_SECONDS
                    )
                    time.sleep(wait)
                    continue
                raise RuntimeError(
                    f"Claude API call failed after {self._max_retries} retries: {exc}"
                ) from exc

            except anthropic.BadRequestError as exc:
                # Do not retry on bad requests (e.g. content filter, prompt too long)
                raise RuntimeError(
                    f"Claude API rejected the request: {exc}"
                ) from exc

        # Should not be reached, but guard against empty retry loop
        raise RuntimeError(
            f"Claude API call failed: {last_error}"
        )


