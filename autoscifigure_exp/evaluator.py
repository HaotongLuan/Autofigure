"""
VLM-as-Judge evaluation framework for automated scientific figure generation.

Uses the Anthropic Claude API to evaluate generated SVG scientific figures
against source text and optional ground truth annotations. Supports single
evaluation, blind A/B comparison, and batch evaluation with aggregated statistics.
"""

