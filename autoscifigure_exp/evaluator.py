def _truncate_svg(svg: str, max_len: int = MAX_SVG_LENGTH) -> str:
    """Truncate SVG content to fit within context limits while preserving structure.

    If truncation is needed, we keep the beginning (which contains <svg> attributes,
    <defs>, and initial elements) and the end (closing tags), removing the middle.
    """
    if len(svg) <= max_len:
        return svg

    # Keep 70% from the start, 20% from the end, and insert a truncation notice
    head_ratio = 0.70
    tail_ratio = 0.20
    head_len = int(max_len * head_ratio)
    tail_len = int(max_len * tail_ratio)
    half_gap = (max_len - head_len - tail_len) // 2

    head = svg[:head_len]
    tail = svg[-tail_len:]

    notice = (
        f"\n<!-- [{half_gap} characters omitted for length; "
        f"original SVG was {len(svg)} chars] -->\n"
    )

    return head + notice + tail


def _extract_json_from_response(text: str) -> str:
    """Extract a JSON object from a response that may contain markdown fences
    or surrounding text.

    Tries:
    1. Direct JSON parse of the full text.
    2. Extract content between ```json ... ``` fences.
    3. Extract content between { and } braces (greedy outermost).
    """
    text = text.strip()

    # Try direct parse
    try:
        json.loads(text)
        return text
    except json.JSONDecodeError:
        pass

    # Try fenced code block
    fence_patterns = [
        r"```json\s*([\s\S]*?)\s*```",
        r"```\s*([\s\S]*?)\s*```",
    ]
    for pat in fence_patterns:
        m = re.search(pat, text)
        if m:
            candidate = m.group(1).strip()
            try:
                json.loads(candidate)
                return candidate
            except json.JSONDecodeError:
                continue

    # Try balanced braces extraction
    # Find the first { and the matching last }
    first_brace = text.find("{")
    if first_brace >= 0:
        depth = 0
        last_brace = first_brace
        for i, ch in enumerate(text[first_brace:], start=first_brace):
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0:
                    last_brace = i
                    break
        if last_brace > first_brace:
            candidate = text[first_brace : last_brace + 1]
            try:
                json.loads(candidate)
                return candidate
            except json.JSONDecodeError:
                pass

    return text


def _validate_scores(result: dict[str, Any], dims: tuple[str, ...]) -> dict[str, Any]:
    """Ensure all score dimensions are present, integers, and in range [0, 10]."""
    for dim in dims:
        if dim not in result:
            result[dim] = 0
        else:
            try:
                val = int(result[dim])
                result[dim] = max(0, min(10, val))
            except (ValueError, TypeError):
                result[dim] = 0

    # Ensure issues is a list
    if "issues" not in result or not isinstance(result["issues"], list):
        result["issues"] = []

    # Clean issues
    cleaned_issues = []
    for issue in result["issues"]:
        if not isinstance(issue, dict):
            continue
        cleaned_issues.append({
            "severity": issue.get("severity", "medium")
            if issue.get("severity") in SEVERITY_LEVELS
            else "medium",
            "category": issue.get("category", "other"),
            "description": issue.get("description", ""),
        })
    result["issues"] = cleaned_issues

    if "summary" not in result:
        result["summary"] = ""

    return result


def _compute_stats(values: list[float]) -> dict[str, float]:
    """Compute mean and standard deviation for a list of numeric values."""
    n = len(values)
    if n == 0:
        return {"mean": 0.0, "std": 0.0, "min": 0.0, "max": 0.0, "count": 0}

    mean = sum(values) / n
    variance = sum((x - mean) ** 2 for x in values) / n
    std = math.sqrt(variance)
    return {
        "mean": round(mean, 2),
        "std": round(std, 2),
        "min": round(min(values), 2),
        "max": round(max(values), 2),
        "count": n,
    }


